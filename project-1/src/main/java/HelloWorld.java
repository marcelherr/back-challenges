public class HelloWorld {

    public static void main(String[] args) {

        System.out.println("Java is cool!");

        System.out.println("Coding: Relational Operators");

        // Integers
        System.out.println("Int");
        int a = 4;
        int b = 3;

        System.out.println("Sum: " + (a + b));
        System.out.println("Diff: " + (a - b));
        System.out.println("Multiply: " + (a * b));
        System.out.println("Divide: " + (a / b));
        System.out.println("a is greater than b: " + (a > b));
        System.out.println("a is less than b: " + (a < b));
        System.out.println("a is equal to b: " + (a == b));

        // Floats
        System.out.println("Float");
        float c = 6.74f;
        float d = 4.84f;

        System.out.println("Sum: " + (c + d));
        System.out.println("Diff: " + (c - d));
        System.out.println("Multiply: " + (c * d));
        System.out.println("Divide: " + (c / d));
        System.out.println("c is greater than d: " + (c > d));
        System.out.println("c is less than d: " + (c < d));
        System.out.println("c is equal to d: " + (c == d));

        // Doubles
        System.out.println("Double");
        double e = 9.234523;
        double f = 6.7658675;

        System.out.println("Sum: " + (e + f));
        System.out.println("Diff: " + (e - f));
        System.out.println("Multiply: " + (e * f));
        System.out.println("Divide: " + (e / f));
        System.out.println("e is greater than f: " + (e > f));
        System.out.println("e is less than f: " + (e < f));
        System.out.println("e is equal to f: " + (e == f));
    }
}
